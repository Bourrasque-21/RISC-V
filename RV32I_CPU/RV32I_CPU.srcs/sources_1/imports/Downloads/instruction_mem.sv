`timescale 1ns / 1ps

module instruction_mem (
    input  [31:0] instr_addr,
    output [31:0] instr_data
);

    logic [31:0] rom[0:31];

    initial begin  // funct7 | rs2 | rs1 | funct3 | rd | opcode // [ADD] rd, rs1, rs2
        // SLT x10, x1, x2   (-1 < 1)
        rom[0] = 32'b0000000_00010_00001_010_01010_0110011;

        // SLTU x11, x1, x2  (unsigned 비교)
        rom[1] = 32'b0000000_00010_00001_011_01011_0110011;
        // SRL x12, x3, x4  (-8 >> 2 logical)
        rom[2] = 32'b0000000_00100_00011_101_01100_0110011;

        // SRA x13, x3, x4  (-8 >> 2 arithmetic)
        rom[3] = 32'b0100000_00100_00011_101_01101_0110011;
        // rom[0] = 32'b0000000_00001_00010_000_01010_0110011;  // ADD  x10, x2,  x1
        // rom[1] = 32'b0100000_00010_00011_000_01011_0110011;  // SUB  x11, x3,  x2
        // rom[2] = 32'b0000000_00011_00100_001_01100_0110011;  // SLL  x12, x4,  x3
        // rom[3] = 32'b0000000_00100_00101_010_01101_0110011;  // SLT  x13, x5,  x4
        rom[4] = 32'b0000000_00101_00110_011_01110_0110011;  // SLTU x14, x6,  x5
        rom[5] = 32'b0000000_00110_00111_100_01111_0110011;  // XOR  x15, x7,  x6
        rom[6] = 32'b0000000_00111_01000_101_10000_0110011;  // SRL  x16, x8,  x7
        rom[7] = 32'b0100000_01000_01001_101_10001_0110011;  // SRA  x17, x9,  x8
        rom[8] = 32'b0000000_01001_01010_110_10010_0110011;  // OR   x18, x10, x9
        rom[9] = 32'b0000000_01010_01011_111_10011_0110011;  // AND  x19, x11, x10
        rom[10] = 32'h00000000;
        rom[11] = 32'h00000000;
        rom[12] = 32'h00000000;
        rom[13] = 32'h00000000;
        rom[14] = 32'h00000000;
        rom[15] = 32'b0000000_00010_00001_010_01010_0110011;  // SLT x10, x1, x2
        rom[16] = 32'b0000000_00010_00001_011_01011_0110011; // SLTU x11, x1, x2
        rom[17] = 32'h00000000;
        rom[18] = 32'b0000000_00100_00011_101_01100_0110011; // SRL x12, x3, x4 
        rom[19] = 32'b0100000_00100_00011_101_01101_0110011; // SRA x13, x3, x4 
        rom[20] = 32'h00000000;
        rom[21] = 32'h00000000;
        rom[22] = 32'h00000000;
        rom[23] = 32'h00000000;
        rom[24] = 32'h00000000;
        rom[25] = 32'h00000000;
        rom[26] = 32'h00000000;
        rom[27] = 32'h00000000;
        rom[28] = 32'h00000000;
        rom[29] = 32'h00000000;
        rom[30] = 32'h00000000;
        rom[31] = 32'h00000000;
    end

    assign instr_data = rom[instr_addr[6:2]];

endmodule
